void sort_int32( u32 *data , u32 size ){

    unsigned int count = (unsigned int) size -1;
    register u32 max = 0;
    register u32 index = 0;

    while (count > 0){
        for(u32 i = 0; i < count; i++){
            if (data[i] >= max){
                max = data[i];
                index = i;
            }
        }
        data[index] = data[count];
        index = 0;
        data[count] = max;
        max = 0;

        count --;
    }
}

unsigned int is_sorted( u32 *data , u32 size){

    u32 last = data[0];
    u32 current = data[1];

    for(u32 i = 2; i < size; i++){
        if( current < last ){
            return 0;
        }else{
            last = current;
            current = data[i];
        }
    }
    return 1;
}


